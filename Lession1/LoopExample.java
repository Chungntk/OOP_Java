public class LoopExample
{
	public static void main(String args[])
	{
		// for
		// while
		// do while
		for(int i=10; i < 10; i++)
			System.out.println(i);
		
		int j = 10;
		while(j < 10)
		{
			System.out.println(j);
			j++;
		}
		
		int k=10;
		do{
			System.out.println(k);
			k++;
		}while(k<10);
		
	}
}